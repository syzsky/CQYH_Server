				if (主程.当前时间 < this.封禁日期.V)
				{
					当前网络.发送封包(new 登陆错误提示
					{
						错误代码 = 285u,
						参数一 = 计算类.时间转换(this.封禁日期.V)
					});
					return;
				}
				if (主程.当前时间 < 角色数据2.封禁日期.V)
				{
					当前网络.发送封包(new 登陆错误提示
					{
						错误代码 = 285u,
						参数一 = 计算类.时间转换(角色数据2.封禁日期.V)
					});
					return;
				}
				当前网络.SendRaw(1003, 6, new byte[4]
				{
					(byte)((uint)角色数据2.角色编号 & 0xFFu),
					(byte)((uint)(角色数据2.角色编号 >> 8) & 0xFFu),
					(byte)((uint)(角色数据2.角色编号 >> 16) & 0xFFu),
					(byte)((uint)(角色数据2.角色编号 >> 24) & 0xFFu)
				});
				当前网络.绑定角色 = new 玩家实例(角色数据2, 当前网络);
				当前网络.当前阶段 = 游戏阶段.场景加载;
